    private void comp() {
        String palabra;
        palabra = txtPalabra.getText();
        try {
            Pattern pat = Pattern.compile("A[a-z]{4,6}");
            Matcher mat = pat.matcher(palabra);

            if (mat.matches()) {
                Object[] dat = {palabra};
                tblmodel.addRow(dat);
                tblDatos.setModel(tblmodel);
            } else {
                JOptionPane.showMessageDialog(null, "Incorrecto");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la expresi√≥n regular: " + e.getMessage());
        }
    }

    private void mandar() {
        txtPalabra.setText("");
    }

